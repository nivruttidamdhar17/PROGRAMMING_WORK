a = 9
b = 4

# Arithmetic Operators in python
print("a + B =", a + b)
print("a - B =", a - b)
print("a * B =", a * b)
print("a / B =", a / b)

# Assignment Operators in python
print("Demonstrating Assignment Operators in Python")
c = 5
d = 7

print(c, d)
c += 4
d += 1
print(c, d)


# Comparison Operators in python
print("Demonstrating Comparison Operators in Python")
e = 6
f = 9

print(e==f)
print(e>f)
print(e<f)
print(e!=f)

# Logical Operations in Python
print("Demonstrating Logical Operations in Python")
e = 6
f = 9

print(e==f and e>f)
print(e==f or e<f)
print(not(e>f))
